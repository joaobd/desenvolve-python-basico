# Armazenando os valores nas variáveis
var1 = 5
var2 = 2

# Imprimindo o tipo de dados das variáveis
print("O tipo de var1 é:", type(var1))
print("O tipo de var2 é:", type(var2))

# Dividindo var1 por var2 e armazenando o resultado em uma terceira variável
var3 = var1 / var2

# Imprimindo o resultado e o tipo de var3
print("O resultado da divisão é:", var3)
print("O tipo de var3 é:", type(var3))